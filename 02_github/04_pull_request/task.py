# Task: Pull Request practice

# 1. Created a separate feature branch for the PR.
#    Command:
#    git switch -c feature/pr-demo

# 2. Added changes in changes.txt and supporting files.
#    Commands:
#    git add .
#    git commit -m "Add PR demo changes"

# 3. Pushed the feature branch to GitHub with upstream tracking.
#    Command:
#    git push -u origin feature/pr-demo

# 4. Opened a Pull Request on GitHub using the provided link.

# 5. Reviewed the diff in the “Files changed” tab and added a comment.

# 6. Merged the Pull Request into main through the GitHub interface.

# 7. Deleted the feature branch on GitHub after merge.
#
# Result:
# A full Pull Request workflow was completed: branch → changes → push →
# PR → review → merge → cleanup.